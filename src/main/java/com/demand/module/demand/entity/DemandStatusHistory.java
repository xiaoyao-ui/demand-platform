package com.demand.module.demand.entity;

import lombok.Data;
import java.time.LocalDateTime;

/**
 * 需求状态变更历史记录实体类
 * <p>
 * 对应数据库 {@code demand_status_history} 表，记录需求每次状态变更的详细信息。
 * 用于审计追踪和故障排查，形成完整的状态流转时间轴。
 * </p>
 * 
 * <h3>典型数据示例：</h3>
 * <pre>
 * +----+-----------+------------+------------+---------------------+--------------+----------------+---------------------+
 * | id | demand_id | old_status | new_status | remark              | operator_id  | operator_name  | create_time         |
 * +----+-----------+------------+------------+---------------------+--------------+----------------+---------------------+
 * |  1 |       100 |          6 |          0 | 提交审核             |           10 | 张三           | 2026-04-23 10:00:00 |
 * |  2 |       100 |          0 |          1 | 需求描述清晰，同意   |           15 | 李四(经理)     | 2026-04-23 11:00:00 |
 * |  3 |       100 |          1 |          2 | 批量分配负责人       |           15 | 李四(经理)     | 2026-04-23 11:30:00 |
 * |  4 |       100 |          2 |          3 | 开发完成，进入测试   |           20 | 王五           | 2026-04-25 16:00:00 |
 * |  5 |       100 |          3 |          4 | 测试通过             |           20 | 王五           | 2026-04-27 10:00:00 |
 * +----+-----------+------------+------------+---------------------+--------------+----------------+---------------------+
 * </pre>
 * 
 * <h3>状态码对照表：</h3>
 * <ul>
 *   <li><b>0</b> - 待审批</li>
 *   <li><b>1</b> - 审批通过</li>
 *   <li><b>2</b> - 开发中</li>
 *   <li><b>3</b> - 测试中</li>
 *   <li><b>4</b> - 已完成</li>
 *   <li><b>5</b> - 已拒绝</li>
 *   <li><b>6</b> - 草稿</li>
 * </ul>
 * 
 * <h3>使用场景：</h3>
 * <ul>
 *   <li>需求详情页展示状态流转历史</li>
 *   <li>审计追踪：查看谁在什么时间修改了状态</li>
 *   <li>故障排查：定位状态异常变更的原因</li>
 *   <li>数据统计：分析平均审批时长、开发周期等指标</li>
 * </ul>
 */
@Data
public class DemandStatusHistory {

    /**
     * 历史记录 ID（主键）
     */
    private Long id;

    /**
     * 需求 ID（外键）
     * <p>
     * 关联到 {@code demand} 表，标识此记录属于哪个需求
     * </p>
     */
    private Long demandId;

    /**
     * 变更前状态
     * <p>
     * 状态变更前的旧状态码，例如：6（草稿）
     * </p>
     */
    private Integer oldStatus;

    /**
     * 变更后状态
     * <p>
     * 状态变更后的新状态码，例如：0（待审批）
     * </p>
     */
    private Integer newStatus;

    /**
     * 变更原因/备注
     * <p>
     * 记录状态变更的原因或说明，例如：
     * <ul>
     *   <li>"提交审核"</li>
     *   <li>"需求描述清晰，同意开发"</li>
     *   <li>"撤回原因：需求优先级调整"</li>
     *   <li>"重新提交审核"</li>
     * </ul>
     * </p>
     */
    private String remark;

    /**
     * 操作人 ID
     * <p>
     * 关联到 {@code user} 表，记录谁执行了状态变更操作
     * </p>
     */
    private Long operatorId;

    /**
     * 操作人姓名
     * <p>
     * 冗余存储，避免每次查询都关联 {@code user} 表
     * </p>
     */
    private String operatorName;

    /**
     * 创建时间
     * <p>
     * 状态变更发生的时间，精确到秒
     * </p>
     */
    private LocalDateTime createTime;
}
