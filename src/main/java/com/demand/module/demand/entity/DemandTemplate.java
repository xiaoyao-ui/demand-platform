package com.demand.module.demand.entity;

import lombok.Data;
import java.time.LocalDateTime;

/**
 * 需求模板实体类
 * <p>
 * 对应数据库 {@code demand_template} 表，存储标准化的需求描述模板。
 * 用于提高需求创建的规范性和完整性，减少重复输入。
 * </p>
 * 
 * <h3>典型模板示例：</h3>
 * <pre>
 * +----+------------------+--------------+----------------------------------------------------------+------+--------+---------------------+---------------------+
 * | id | name             | category     | content                                                  | sort | status | create_time         | update_time         |
 * +----+------------------+--------------+----------------------------------------------------------+------+--------+---------------------+---------------------+
 * |  1 | 功能需求模板      | 功能         | ## 一、需求背景...                                       |    1 |      1 | 2026-04-01 10:00:00 | 2026-04-01 10:00:00 |
 * |  2 | Bug修复模板       | Bug          | ## 一、问题描述...                                       |    2 |      1 | 2026-04-01 10:00:00 | 2026-04-01 10:00:00 |
 * |  3 | 性能优化模板      | 优化         | ## 一、当前性能指标...                                   |    3 |      0 | 2026-04-01 10:00:00 | 2026-04-01 10:00:00 |
 * +----+------------------+--------------+----------------------------------------------------------+------+--------+---------------------+---------------------+
 * </pre>
 * 
 * <h3>使用流程：</h3>
 * <ol>
 *   <li>管理员在后台创建多个模板（如"功能需求"、"Bug修复"、"性能优化"）</li>
 *   <li>用户创建需求时，从下拉框选择合适的模板</li>
 *   <li>系统自动将模板内容填充到 Markdown 编辑器</li>
 *   <li>用户根据模板结构填写具体内容</li>
 * </ol>
 */
@Data
public class DemandTemplate {

    /**
     * 模板 ID（主键）
     */
    private Long id;

    /**
     * 模板名称
     * <p>
     * 用于前端展示的中文名称，例如："功能需求模板"、"Bug修复模板"
     * </p>
     */
    private String name;

    /**
     * 模板分类
     * <p>
     * 用于对模板进行分组管理，例如：
     * <ul>
     *   <li>"功能" - 功能需求类模板</li>
     *   <li>"Bug" - Bug修复类模板</li>
     *   <li>"优化" - 性能优化类模板</li>
     * </ul>
     * </p>
     */
    private String category;

    /**
     * 模板内容（Markdown 格式）
     * <p>
     * 定义标准的需求描述结构，包含占位符提示用户填写。
     * </p>
     * 
     * <p>
     * <b>示例</b>：
     * <pre>
     * ## 一、需求背景
     * （请描述需求的业务背景和痛点）
     * 
     * ## 二、需求目标
     * （请描述需求要解决的问题和达到的目标）
     * 
     * ## 三、功能列表
     * 1. 功能点1
     * 2. 功能点2
     * 
     * ## 四、验收标准
     * （请描述需求完成的验收条件）
     * </pre>
     * </p>
     */
    private String content;

    /**
     * 排序号
     * <p>
     * 数值越小越靠前，用于控制模板在下拉框中的显示顺序
     * </p>
     */
    private Integer sort;

    /**
     * 模板状态
     * <ul>
     *   <li>0 - 禁用（不显示在前端）</li>
     *   <li>1 - 启用</li>
     * </ul>
     */
    private Integer status;

    /**
     * 创建时间
     */
    private LocalDateTime createTime;

    /**
     * 更新时间
     */
    private LocalDateTime updateTime;
}
