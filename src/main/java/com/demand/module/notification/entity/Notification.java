package com.demand.module.notification.entity;

import lombok.Data;
import java.time.LocalDateTime;

/**
 * 通知消息实体类
 * <p>
 * 对应数据库 {@code notification} 表，存储系统中的通知消息。
 * 通过 WebSocket 实时推送给在线用户，离线用户登录后自动加载。
 * </p>
 * 
 * <h3>通知类型（type）：</h3>
 * <ul>
 *   <li><b>1 - 需求审批</b>：项目经理审批通过或驳回需求</li>
 *   <li><b>2 - 任务分配</b>：负责人被分配新任务</li>
 *   <li><b>3 - 评论回复</b>：有人回复了你的评论</li>
 *   <li><b>4 - 状态变更</b>：需求状态发生变更</li>
 *   <li><b>5 - 系统通知</b>：系统公告或维护通知</li>
 * </ul>
 * 
 * <h3>典型数据示例：</h3>
 * <pre>
 * +----+--------+-------------+------------------+---------------------+------+------------+--------+---------------------+
 * | id | user_id| receiver_id | title            | content             | type | related_id | is_read| create_time         |
 * +----+--------+-------------+------------------+---------------------+------+------------+--------+---------------------+
 * |  1 |     10 |          20 | 需求审批通过      | 您的需求"用户登录"已通过审批 |    1 |        100 |      0 | 2026-04-23 10:30:00 |
 * |  2 |     15 |          20 | 新任务分配        | 您被分配为"订单模块"的负责人  |    2 |        101 |      0 | 2026-04-23 11:00:00 |
 * +----+--------+-------------+------------------+---------------------+------+------------+--------+---------------------+
 * </pre>
 */
@Data
public class Notification {

    /**
     * 通知 ID（主键）
     */
    private Long id;

    /**
     * 发送人 ID（操作人）
     * <p>
     * 触发通知的用户，例如：
     * <ul>
     *   <li>审批需求的项目经理</li>
     *   <li>分配任务的管理员</li>
     *   <li>回复评论的用户</li>
     * </ul>
     * </p>
     */
    private Long userId;

    /**
     * 接收人 ID
     * <p>
     * 通知的目标用户，即需要接收此消息的人。
     * </p>
     */
    private Long receiverId;

    /**
     * 通知标题
     * <p>
     * 简短概括通知内容，例如："需求审批通过"、"新任务分配"
     * </p>
     */
    private String title;

    /**
     * 通知内容
     * <p>
     * 详细描述通知的具体信息，例如："您的需求'用户登录功能'已通过审批"
     * </p>
     */
    private String content;

    /**
     * 通知类型
     * <ul>
     *   <li>1 - 需求审批</li>
     *   <li>2 - 任务分配</li>
     *   <li>3 - 评论回复</li>
     *   <li>4 - 状态变更</li>
     *   <li>5 - 系统通知</li>
     * </ul>
     */
    private Integer type;

    /**
     * 关联 ID
     * <p>
     * 与通知相关的业务对象 ID，例如：
     * <ul>
     *   <li>需求审批：关联的需求 ID</li>
     *   <li>任务分配：关联的需求 ID</li>
     *   <li>评论回复：关联的评论 ID</li>
     * </ul>
     * </p>
     * 
     * <p>
     * <b>用途</b>：前端点击通知后可直接跳转到对应的业务页面
     * </p>
     */
    private Long relatedId;

    /**
     * 是否已读
     * <ul>
     *   <li>0 - 未读</li>
     *   <li>1 - 已读</li>
     * </ul>
     */
    private Integer isRead;

    /**
     * 创建时间
     * <p>
     * 通知生成的时间，精确到秒
     * </p>
     */
    private LocalDateTime createTime;
}
